#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*tmp;

	tmp = (char *)str;
	while (n > 0)
	{
		*tmp = (char)c;
		tmp++;
		n--;
	}
	return (str);
}
